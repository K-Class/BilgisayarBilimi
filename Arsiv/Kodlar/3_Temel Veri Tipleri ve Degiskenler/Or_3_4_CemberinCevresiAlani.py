# Çemberin Çevresi ve Alanı
print("Yarı Çapı Girilen Çemberin Çevresini ve Alanını Bulma")
r = float(input("Çemberin Yarı Çapını Girin:"))
pi = 3.14159

print("Çemberin Çevresi:", 2*pi*r)
print("Çemberin Alanı  :", pi*(r**2))

